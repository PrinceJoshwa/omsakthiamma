//       <p>உலகமே சிவப்பாகி, சிவப்பு மஞ்சளாகி, மஞ்சள் பச்சையாக மாறுகின்ற வாய்ப்பைக் கொடுக்க போகிறேன்.</p>

//       <p>ஆன்மிகம் என்பது எளிமையும் வலிமையும் கொண்டது.</p>

//       <p>ஆன்மா எப்பொழுது நினைக்கிறதோ அப்பொழுதுதான் உடலை விட்டுப் போகும்.</p>

//       <p>ஆன்மிகத்தில் அவ்வப்போது கிடைக்கின்ற வாய்ப்புகளைப் பயன்படுத்தித் தான் பலன் கிடைக்கும்.</p>

//       <p>உணர்ச்சி வசப்படுதல், கோபப்படுதல் ஆகிய குணங்கள் ஒருவன் தன்னையே அழித்து விடும்.</p>

//       <p>இயற்கையில் உருவாகும் கல் அம்மனாகவும், சிவனாகவும் மாறுகிறது. அதுபோல மனிதனும் முயன்றால் தெய்வமாக மாறலாம்.</p>

//       <p>மருத்துவம், விவசாயம் ஆகிய எல்லாத் துறைகளுக்கும் கடல், ஆகாயம் போன்ற இயற்கை அவசியம்.</p>

//       <p>ஆன்மிகம், தொண்டு, தருமம் ஆகியவற்றில் சோம்பேறித்தனம் ஏற்பட்டால் அழிவு ஏற்படும்.</p>

//       <p>ஆலயத்தினால் உனக்குப் பெருமையே தவிர உன்னால் ஆலயத்துக்குப் பெருமை இல்லை.</p>

//       <p>ஆரம்பத்தில் எப்படி வந்து செயல்பட்டீர்களோ, அவ்வாறே இப்பொழுதும் செயல்பட வேண்டும்.</p>

//       <p>முடியாது என்று எதையும் சொல்லக்கூடாது.</p>

//       <p>உன்னை நினைக்க வைப்பதும் நான். நினைவாக இருப்பதும் நான்தான்.</p>

//       <p>வெளியில் இருப்பவன் உள்ளே வரமுடியும். உள்ளே இருப்பவன் வெளியே போனால் திரும்பி வரமுடியாது.</p>

//       <p>ஓய்வு நேரத்தை ஆன்மிகத்துக்காகப் பயன்படுத்த வேண்டும்.</p>

//       <p>ஆன்மிகத்தில் வழிகாட்டியாகவும் ஒழுக்கமாகவும் இருக்க வேண்டும்.</p>

//       <p>அரசியல் பற்றிக் கவலைப்படாமல் எண்ணத்தில் உறுதியாக இருக்க வேண்டும்.</p>

//       <p>ஒரே எண்ணம்‚ ஒரே பார்வையில் இருக்க வேண்டும்.</p>

//       <p>உழைப்பு குறைந்து போய் விட்டதால் பற்றாக்குறை அதிகமாகி விட்டது.</p>

//       <p>உனது தேவைகள், உனது வேதனைகள், உனது சோதனைகள் ஆகியவற்றை என்னிடம் விட்டு விடு.</p>

//       <p>ஆன்மா என்ற பாத்திரத்தைச் சுத்தமாக வைத்துக் கொள்ள வேண்டும்.</p>

//       <p>நான் கூறுவனவற்றைத் தலையாட்டிக் கேட்டால் மட்டும் போதாது. பொறுப்பேற்றுச் செய்யவும் வேண்டும்.</p>

//       <p>உங்களுக்கு நான் கற்றுக் கொடுக்கும் ஆன்மிகத்தை நீங்கள் பலருக்கும் கற்றுக் கொடுக்க வேண்டும்.</p>

//       <p>பணத்திற்காகச் செய்யும் தொண்டு பிணத்துக்குச் சமம். பக்தியோடு செய்யும் தொண்டுதான் உண்மையானது.</p>

//       <p>தொண்டு செய்யாவிட்டால் துண்டு விழும். உண்மையான தொண்டு வெளிச்சத்துக்கு வரும். பொய்யான தொண்டு வெளிச்சத்துக்கு வராது.</p>

//       <p>நீ செய்கிறாய், நான் கொடுக்கிறேன். நான் கொடுப்பதை நீ நான்கு பேருக்குக் கொடுக்க வேண்டும். ஆனால் யாரையும் கெடுக்கக் கூடாது.</p>

//       <p>செவ்வாடை அணிபவர்களுக்குச் சேவை மனப்பான்மையும், செவிலித்தாய் மனப்பான்மையும் இருக்க வேண்டும்.</p>

//       <p>எப்பொழுதும் சந்தோஷத்துடனும், உண்மை உணர்வோடும் தொண்டு செய்ய வேண்டும்.</p>

//       <p>நீ இருக்கும் வரை வாழ்வதற்கு நான் வழிகாட்டியாக இருக்கிறேன்.</p>

//       <p>அமைதியாக வாழ்ந்து விட்டுப் போவெளியில் இருப்பவன் உள்ளே வருவான். உள்ளே இருப்பவன் வெளியில் போகாமல் பார்த்துக் கொள்.</p>

//       <p>கணவன் மனைவியாகப் பொதுத் தொண்டில் ஈடுபடுவது கடினம். ஆனாலும் அப்படி ஈடுபட்டால் நல்ல பயன் உண்டு.</p>

//       <p>ஆதிபராசக்தி வழிபாட்டு மன்றங்களில் சென்று வழிபட்டாலே பயன் உண்டு.</p>

//       <p>ஆன்மிகத்தில் தொண்டுதான் முக்கியம்.</p>

//       <p>அடிக்கடி தூசு தட்டுவது போல, தொண்டு, தருமம் உள்ளம் ஆகியவற்றை தட்டி எழுப்ப வேண்டும்.</p>

//       <p>தருமம், தொண்டு செய்து வந்தால் எந்த ஆபத்தும் இல்லை. ஆனால் பொறாமையும் அழுக்கும் சேர்ந்தால் ஆபத்து.</p>

//       <p>அம்மா இருக்கிறாள் என்று பாம்பு, தேள் இருக்கும் இடத்தில் கை வைக்கக் கூடாது.</p>

//       <p>அம்மா சொல்லிவிட்டார்களே என்று கடனே என்றும் செய்யக் கூடாது.</p>

//       <p>சாதாரண நிலையிலும் நான் அடிகளாரோடு இருப்பேன்.</p>

//       <p>ஆயிரத்தெட்டு முறை பாலகன் பார்வை எவன் எவன் மீது படுகின்றதோ அவனுக்கெல்லாம் ஜென்ம சாபல்யமடா.</p>

//       <p>நீ அடிகளாரிடம் கற்றுக்கொள்ள முடியாததை வேறெங்கும் சென்று கற்றுக்கொள்ள முடியாது.</p>

//       <p>நம் சித்தர் பீடத்தில் ஏழை பணக்காரன் அனைவரும் சமம். இவ்விடத்தில் பெண் சக்திகட்கு முன்னுரிமை உண்டு.</p>

//       <p>இங்கே வந்தால் உங்கட்குப் பணம் காசு கிடைக்கிறதோ இல்லையோ, மன அமைதி நிச்சயம் கிடைக்கிறது.</p>

//       <p>பக்தர்கட்கு அம்மாவின் பிரசாதங்களான குங்குமம், திருநீறு கொடுக்கும்போது, பயபக்தியுடன் கொடுக்க வேண்டும். எடுத்து வீசுவது போல் வழங்கக் கூடாது.</p>

//       <p>தெய்வத்துக்குச் செலவு செய்து யாரும் கடனாளியாவது இல்லை.</p>

//       <p>மனிதர்க்குக் கடன் கொடுத்தவன் தான் இன்றைய பிச்சைக்காரன்.</p>

//       <p>தெய்வத்துக்குச் செலவு செய்து கெட்டவன் இல்லை மனிதர்க்குச் செலவு செய்து வாழ்ந்து கெட்டவர் உண்டு.</p>

//       <p>பெண்கள் நகைக்காக அதிகம் செலவு செய்கிறார்கள். நகையால் ஆபத்துக்கள் தான் மிஞ்சும்.</p>

//       <p>அம்மா நம்மை வளர்க்கிறபோதே நாமும் நாலுபேரை வளர்க்கணும். கூடவே தான தருமங்களைச் செய்ய வேண்டும்.</p>

//       <p>வேள்விக்குழு‚ பிரச்சாரக்குழு‚ இளைஞர் அணி‚ மூன்றும் சூலத்தின் மூன்று கிளைகள் போன்றவை.</p>

//       <p>அதனைத் தாங்குகிற தாங்கிதான் நம் அம்மா. நம்மைத் தாங்கி நிற்பது அம்மாதான் என்பதை மறந்து விடக்கூடாது.</p>

//       <p>நம் தொண்டர்கள் ஒரு குடும்பம் மாதிரி, ஆன்மிகத்தில் இணைந்து தொண்டுகள் செய்து வர வேண்டும்.</p>

//       <p>முதியவர்களிடம் அனுபவம் மிகுதி‚ எடுத்துச் செய்யும் ஆற்றல் இளைஞர்களிடம் மிகுதி‚ உங்கள் மனசு சுத்தமாக இருக்க வேண்டும்.</p>

//       <p>உங்கள் உள் மனமும் சுத்தமாக இருக்க வேண்டும். நீங்கள் மற்றும் உங்கள் குடும்பம் நன்றாக இருந்தால்தான் ஆலயமும் நன்றாக இருக்கும்.</p>

//       <p>ஆலயம் நன்றாக இருந்தால் தான் இயற்கையும் நன்றாக இருக்கும். இயற்கை நன்றாக இருந்தால் தான் பஞ்ச பூதமும் நன்றாக இருக்கும்.</p>

//       <p>பஞ்ச பூதங்கள் நல்லவனா கெட்டவனா என்று பார்ப்பதில்லை. அம்மாவும் அது மாதிரிதான்.</p>

//       <p>பஞ்ச பூதங்கள் எல்லோர்க்கும் பொது. அம்மாவும் அது மாதிரி எல்லோர்க்கும் பொது.</p>

//       <p>வழிபாட்டை உரிய முறையில் மன ஒருமையோடும் செய்தால் தான் பலன் கிடைக்கும்.</p>

//       <p>தொண்டு செய்யும் போது தானாகவே மனம் அதில் லயித்து விடுவதால் பலனும் உடனே கிடைக்கிறது.</p>

//       <p>ஒவ்வொருவரும் உள்ளேயும் அம்மா இருக்கிறாள். எனக்குத் தெரியும்.</p>

//       <p>உங்கள் முற்பிறவி, உங்கள் துன்பம், அதற்கான காரணங்கள் எல்லாம் தான் தெரிகின்றன.</p>

//       <p>நம் இயக்கத்திலும் பெண்களிடம் ஆர்வமும் எழுச்சியும் அதிகம். அவர்களுக்குத் அதிக முக்கியத்துவம் கொடுக்க வேண்டும்.</p>

//       <p>அம்மா என்னிடம் மட்டும் இல்லை‚ உங்கள் ஒவ்வொருவர் உள்ளேயும் அம்மா இருக்கிறாள்.</p>

//       <p>தியானம் செய்வதை விடத் தொண்டு செய்வது சுளுவானது.</p>

//       <p>உங்கள் மனத்தை ஒருமுகப்படுத்தித் தியானம் செய்யும்போது அம்மா அடக்கத்தைப் பின்பற்ற வேண்டி வரும்.</p>

//       <p>விழாக்களைக் கொண்டாடுவதன் மூலந்தான் பக்தர்களும் அன்பும், பக்தியும் வளர்வதற்கு வாய்ப்பு ஏற்படுகிறது.</p>

//       <p>எந்த விழாவைக் கொண்டாடினாலும் உள்ளத்தில் உண்மை உணர்வும், பக்தி உணர்வும் கூடிய பண்பு தேவை.</p>

//       <p>விழாக்கள் என்பதெல்லாம் உங்களுக்குத்தானே தவிர, உங்களுக்குள்ளே இருக்கும் ஆன்மாவிற்கு அல்ல.</p>

//       <p>இங்கு நடைபெறும் விழாக்கள் எல்லாம் உங்களுடைய எதிர்கால வளர்ச்சிக்காக.</p>

//       <p>விழாக்கள் எனக்கோ அடிகளார்க்கோ அல்ல. மரம், செடி வளர்வதற்கும் கவனிப்பு தேவை.</p>

//       <p>அதுபோல ஆன்மிகம் வளர்வதற்கு விழாவும், தருமமும் தேவை.</p>

//       <p>விழாக்கள் மூலமாக வெளிஉலக அனுபவங்கள் கிடைப்பதுடன், பாவங்கள் சேராமல் பாதுகாப்பும் கிடைக்கிறது.</p>

//       <p>இங்கு விழாக்களைக் கொண்டாடுவது பெரிதல்ல. வருகிற பக்தர்களுக்குப் பக்குவத்தை உண்டாக்க வேண்டும்.</p>

