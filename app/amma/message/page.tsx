//       <p>நீங்கள் சொல்லும், செயலும் ஒன்றாக இல்லாதபோது, வேதனைகளும் சோதனைகளும் வரும்.</p>

//       <p>ஒருவன் உழைக்கிறான்; ஒன்பது பேர் தூங்குகிறார்கள் — இதுதான் இன்றைய நிலை.</p>

