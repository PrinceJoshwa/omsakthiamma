//       <p>உங்கள் உள்ளம் கலங்கக் கூடாது. அது கறுப்பாக மாறக் கூடாது.</p>

//       <p>உங்கள் பரம்பரையும், பாரம்பரியமும் காக்கப்பட வேண்டும்.</p>

//       <p>சித்திரைப் பௌர்ணமி முக்கியமான பௌர்ணமி. ஏதோ யாக குண்டம், ஏதோ பூஜை என்று நினைக்கக் கூடாது.</p>

//       <p>உலகத்துக்கு ஆன்மிகத்தை எடுத்துக் காட்டத்தான் சித்ரா பௌர்ணமி வேள்வி நடத்தப்படுகிறது.</p>

//       <p>உங்கள் மனத்தை ஒருமுகப்படுத்தும் பயிற்சி பெறவே விழாக்கள் ஏற்பாடு செய்து தருகிறேன்.</p>

//       <p>வழிபாடுதான் மனிதனுக்கு நிம்மதியைக் கொடுக்கும். இறைவழிபாடு மனிதனின் அன்றாடக் கடமை.</p>

//       <p>தியானம்‚ யோகம்‚ என்று இருந்தால் மட்டும் போதாது. உடல் உழைப்பு வேண்டும். அது பலன் கொடுக்கும்.</p>

//       <p>ஆண்டுக்குப் பலன் இருப்பதை விட வேண்டுதலுக்குப் பலன் உண்டு.</p>

//       <p>தத்துவங்களை இனிப்பாகப் பேசலாம். ஆனால் பத்து பேர் ஒன்று சேர்ந்து வழிபாடு செய்வதால் பயன் உண்டு.</p>

//       <p>மனத்தில் ஏற்படும் பொறாமைதான் கண் திருஷ்டியாக மாறுகிறது.</p>

//       <p>எதற்கும் மனம் உடையக் கூடாது.</p>

//       <p>எங்கெங்கே முறையான வழிபாடு நடக்கின்றதோ, அங்கெல்லாம் நல்ல மழை உண்டு.</p>

//       <p>பஞ்ச பூத வழிபாட்டின் மூலம் வேள்வி செய்யும் போது மழை பெய்யும்.</p>

//       <p>ஒவ்வொரு விழாவிற்கும் ஓர் உயிர்ப்புத் தன்மை உண்டு.</p>

//       <p>ஆன்மிகத்தில் இறைவழிபாட்டில் தாய் வழிபாடு சிறந்தது.</p>

//       <p>கண்களைத் திறந்து கொண்டு தியானம் செய்யப் பழக வேண்டும்.</p>

//       <p>உலக வளர்ச்சிக்கும், தியான பயிற்சிக்கும் இங்கு ஏற்பாடு உண்டு.</p>

//       <p>கூட்டு வழிபாடு செய்யும் போது வீட்டைப் பற்றி நினைக்காமல், மன நிம்மதியுடன் வழிபாடு செய்தால் பலன் கிடைக்கும்.</p>

//       <p>மனக்கட்டுப்பாட்டுடன் செய்கின்ற தியானத்தினால் நீங்கள் பல பயன்களை அடையலாம்.</p>

//       <p>திருஷ்டி என்பது மனத் திருப்திக்காகச் செய்யப்படுவது. மனத்தில் உள்ள பதற்றமே திருஷ்டி.</p>

//       <p>சூலத்தில் மூன்று பிரிவுகள் உண்டு: பிறப்பு, இறப்பு, மன்னிப்பு என்ற கருத்தை உணர்த்துகிறது.</p>

//       <p>விலங்குகளும் மனிதர்களும் காட்டில் ஒன்றாக வாழ்ந்த போது பிரச்சனை இல்லை. மனிதன் தன் எண்ணங்களால் விலங்கினமாக மாறுகிறான்.</p>

//       <p>கடன் கொடுத்துப் பணத்தைப் பாதுகாக்கலாம். ஆனால் கடத்தலில் ஈடுபடக் கூடாது.</p>

//       <p>நீங்கள் எந்தப் படிப்பு படித்தாலும் அனுபவப் படிப்பு அவசியம்.</p>

//       <p>நாகரிகம் என்ற பெயரில் விஞ்ஞானம் வளர வளர இயற்கை வளம் மாறுபடுகிறது.</p>

//       <p>வெளிநாடுகளில் இயற்கை வளம் எல்லாம் செயற்கை வளமாக மாறிவருகின்றன.</p>

//       <p>நல்ல பரம்பரை நல்ல உலகத்தை உருவாக்கும். நல்ல ஆன்மா, அமைதி, தாய்மை — இவை நல்ல உலகத்தை உருவாக்கும்.</p>

//       <p>பாத்திரத்தைத் தேய்மானம் வராமல் பாதுகாப்பது போல, உங்கள் மனத்தையும் தேய்மானம் இல்லாமல் பாதுகாக்க வேண்டும்.</p>

//       <p>வந்த பாதையை நினைத்துப் பார்க்க வேண்டும். பொன், பொருள், பெண் ஆசை, ஆணவத்தை ஒழிக்க வேண்டும்.</p>

//       <p>ஒரே குணம்‚ ஒரே செயல் — இவை வாழ்க்கையில் பின்பற்ற வேண்டும்.</p>

//       <p>சக்கரம், தெய்வீகம், ஆன்மிகம் — இவை மனிதனுக்கு அமைதியைக் கொடுக்கும்.</p>

//       <p>வாள் வீச்சு, சொல் வீச்சு எடுபடாது. உள்ளத்தில் உண்மை வீச்சு இருக்க வேண்டும்.</p>

//       <p>வருங்காலத்தில் பூ, பொட்டு, மஞ்சள் — இவை மகிமை பெறும். தங்கத்துக்கு மதிப்பு இருக்காது.</p>

//       <p>நீ எத்தனை சொத்து சேர்த்தாலும் நீ செய்யும் தருமம்தான் நிலையான சொத்து.</p>

//       <p>பிறருக்குக் கொடுக்க வேண்டும். உழைத்து உயர வேண்டும்.</p>

//       <p>ஆன்மிகம்தான் எதிர்காலத்தில் மனிதனுக்கு உதவி செய்யும்.</p>

//       <p>உழைப்பு இல்லாமல் படிப்பையும் விஞ்ஞானத்தையும் நம்பிக் கொண்டிருந்தால் பயன் கிடைக்காது.</p>

