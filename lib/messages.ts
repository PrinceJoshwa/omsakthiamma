export function getDailyMessage() {
  // Use the current date to seed the random selection
  // This ensures the message changes only once every 24 hours
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
  const index = seed % ammaMessages.length;
  return ammaMessages[index];
}